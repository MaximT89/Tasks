package Tasks4;

public class Task3 {
    public static void main(String[] args) {

        /*

        В данной программе дан бесконечный цикл while, также в программе есть переменная t, которая в цикле увеличивается на 1.
        Ваша задача, не трогая условие в цикле, выйти из цикла, когда переменная t станет равна 10.

         */

        int t = 0;

        while(true){
            t++;
            System.out.println("Переменная t равняется " + t);
        }
    }
}
